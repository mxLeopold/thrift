package com.sunlands.rpc.web.biz.model;

import java.math.BigDecimal;

public class TikuUserRecordDTO {
    private Integer id;
    private Integer stuId;
    private String name;
    private Integer sequence;
    private String exerciseType;
    private Integer paperId;
    private Integer unitId;
    private Integer questionNum;
    private Integer totalTime;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getStuId() {
        return stuId;
    }

    public void setStuId(Integer stuId) {
        this.stuId = stuId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getSequence() {
        return sequence;
    }

    public void setSequence(Integer sequence) {
        this.sequence = sequence;
    }

    public String getExerciseType() {
        return exerciseType;
    }

    public void setExerciseType(String exerciseType) {
        this.exerciseType = exerciseType;
    }

    public Integer getPaperId() {
        return paperId;
    }

    public void setPaperId(Integer paperId) {
        this.paperId = paperId;
    }

    public Integer getUnitId() {
        return unitId;
    }

    public void setUnitId(Integer unitId) {
        this.unitId = unitId;
    }

    public Integer getQuestionNum() {
        return questionNum;
    }

    public void setQuestionNum(Integer questionNum) {
        this.questionNum = questionNum;
    }

    public Integer getTotalTime() {
        return totalTime;
    }

    public void setTotalTime(Integer totalTime) {
        this.totalTime = totalTime;
    }

    public BigDecimal getStuTotalScore() {
        return stuTotalScore;
    }

    public void setStuTotalScore(BigDecimal stuTotalScore) {
        this.stuTotalScore = stuTotalScore;
    }

    private BigDecimal stuTotalScore;


}
